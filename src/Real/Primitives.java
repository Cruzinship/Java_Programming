package Real;

public class Primitives {
    public static void main(String[] args) {
        //PRIMITIVES: INT, DOUBLE, CHAR,BOOLEAN!!!(Topic 1)
        //OUT of 8 of the Primitive Data types 6 are used toward numbers
        //Int would be the most used due to the fact that it has a huge range compared to byte and short
        //Double is used for Decimal values
        //Char is used for Single Characters(This includes special ones)
        //Boolean is a True or false statement used for returning said values and great for While loops

        byte numberB = 10;
        short numberS = 200;
        int numberI = 2000;
        long numberL = 20000L;
        double numberWithDecimal = 10.5;
        float numberWithDecimalF = 1234.34f;
        char character = 'C';
        boolean trueOrFalse = true;
        System.out.println("numberB = " + numberB);
        System.out.println("numberS = " + numberS);
        System.out.println("numberI = " + numberI);
        System.out.println("numberL = " + numberL);
        System.out.println("numberWithDecimal = " + numberWithDecimal);
        System.out.println("numberWithDecimal2 = " + numberWithDecimalF);
        System.out.println("character = " + character);
        System.out.println("trueOrFalse = " + trueOrFalse);
    }
}