package day06_ifStatements;

public class MinimumNumber {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 2;

        if(num2 < num1){
            System.out.println(num2 + " Is the minimum number");
        }
        if(num2 > num1){
            System.out.println(num1 + " is the minimum number");
        }
    }
}
